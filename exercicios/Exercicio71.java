package exercicios;

public class Exercicio71 {


    // Retorna o maior numero
    public static int eMaior(int a, int b) {

        if (a > b) {
            return a;
        }

        return b;

    }

    public static void main(String[] args) {

        System.out.println(eMaior(23, 24));

    }

}
